public class Vowel{

    public static void main(String[] args) {

        char ch = 'i' ;

        switch (ch) {
            case 'a':
            case 'e':
            case 'i':
            case 'o':
            case 'u':
                System.out.println(ch + " is vowel");
                break;
                
            
        }
    }
}